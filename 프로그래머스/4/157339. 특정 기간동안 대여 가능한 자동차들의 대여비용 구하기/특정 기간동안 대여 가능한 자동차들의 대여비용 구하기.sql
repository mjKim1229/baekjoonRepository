#자동차 종류 '세단' 'SUV'

#2022-11-01 ~ 11-30까지 대여 가능  & 
# 30일간의 대여 금액이 50만이상 200만 미만인
#"자동차"

#자동차 ID, 자동차 종류, 대여 금액 
#대여 금액 내림차순, 자동차 종류 오름차순, 자동차 ID 내림차순 

WITH DISCOUNT AS (
    SELECT *
    FROM CAR_RENTAL_COMPANY_DISCOUNT_PLAN
    WHERE CAR_TYPE IN ('세단','SUV') AND DURATION_TYPE = '30일 이상'
)

SELECT CAR_ID, CAR_TYPE, ROUND(DAILY_FEE * 30 * ((100-DISCOUNT_RATE)/100)) AS FEE
FROM DISCOUNT INNER JOIN CAR_RENTAL_COMPANY_CAR
USING (CAR_TYPE)
WHERE CAR_ID NOT IN 
(
    SELECT DISTINCT CAR_ID 
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY  WHERE 
    (START_DATE BETWEEN '2022-11-01' AND '2022-11-30') OR 
    (END_DATE BETWEEN '2022-11-01' AND '2022-11-30') OR
    (START_DATE < '2022-11-01' AND END_DATE > '2022-11-30')
)
AND ROUND((DAILY_FEE * 30 * ((100-DISCOUNT_RATE)/100))) > 500000 AND 
ROUND((DAILY_FEE * 30 * ((100-DISCOUNT_RATE)/100))) < 2000000
ORDER BY FEE DESC , CAR_TYPE, CAR_ID DESC