#세단, SUV 
#2022-11-01 ~ 11-30 대여 가능 
#대여 금액 50이상 200미만 
#CAR_ID	CAR_TYPE FEE
WITH DISCOUNT AS 
(
    SELECT CAR_TYPE, DURATION_TYPE, DISCOUNT_RATE 
    FROM CAR_RENTAL_COMPANY_DISCOUNT_PLAN
    WHERE CAR_TYPE IN ('세단','SUV') AND DURATION_TYPE = '30일 이상'
)

SELECT CAR_ID, CAR_TYPE , ROUND((30 * DAILY_FEE * ((100-DISCOUNT_RATE)/100))) AS FEE
FROM CAR_RENTAL_COMPANY_CAR INNER JOIN DISCOUNT 
USING (CAR_TYPE)
WHERE CAR_ID NOT IN 
(
    SELECT DISTINCT CAR_ID 
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE BETWEEN '2022-11-01' AND '2022-11-30'
    OR END_DATE BETWEEN '2022-11-01' AND '2022-11-30'
    OR START_DATE < '2022-11-01' AND END_DATE > '2022-11-30'
) 
AND ROUND((30 * DAILY_FEE * ((100-DISCOUNT_RATE)/100))) < 2000000 
AND ROUND((30 * DAILY_FEE * ((100-DISCOUNT_RATE)/100))) >= 500000 
ORDER BY FEE DESC, CAR_TYPE, CAR_ID DESC
