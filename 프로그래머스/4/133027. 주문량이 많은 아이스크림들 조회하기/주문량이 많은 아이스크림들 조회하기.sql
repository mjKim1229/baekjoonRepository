SELECT FLAVOR
FROM FIRST_HALF AS a 
INNER JOIN
(
    SELECT FLAVOR , SUM(TOTAL_ORDER) AS JULY_SUM
    FROM JULY
    GROUP BY FLAVOR
) AS b 
USING(FLAVOR)  
ORDER BY (TOTAL_ORDER + JULY_SUM) DESC 
LIMIT 3 





