#자동차 종류 트럭 
#대여 기록 별 대여 금액 
#대여 금액 내림차순, 대여 기록 id 내림차순 

WITH DURATION AS 
(
    SELECT HISTORY_ID, CAR_ID, DAILY_FEE, DATEDIFF(END_DATE,START_DATE) +1 AS GAP, 
    CASE 
        WHEN DATEDIFF(END_DATE,START_DATE) +1>= 90 THEN '90일 이상'
        WHEN DATEDIFF(END_DATE,START_DATE) +1>=30 THEN '30일 이상'
        WHEN DATEDIFF(END_DATE,START_DATE) +1 >= 7 THEN '7일 이상'
        ELSE NULL END AS DURATION_TYPE, CAR_TYPE
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    INNER JOIN CAR_RENTAL_COMPANY_CAR
    USING (CAR_ID)
    WHERE CAR_TYPE = '트럭'
) 

SELECT HISTORY_ID, ROUND(DAILY_FEE * GAP * (
    (100-IFNULL(DISCOUNT_RATE,0))/100)) AS FEE 
FROM DURATION 
LEFT OUTER JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P 
USING (DURATION_TYPE, CAR_TYPE)
ORDER BY FEE DESC, HISTORY_ID DESC 