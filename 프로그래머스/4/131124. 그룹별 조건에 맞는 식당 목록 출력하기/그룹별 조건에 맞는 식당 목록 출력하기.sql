#회원 이름 -> M_P 
#서브쿼리 -> MEMBER_ID
#리뷰 텍스트, 리뷰 작성일 -> REST_REVIEW 

SELECT MEMBER_NAME, REVIEW_TEXT, DATE_FORMAT(REVIEW_DATE,'%Y-%m-%d')
FROM MEMBER_PROFILE
JOIN REST_REVIEW 
USING (MEMBER_ID)
WHERE MEMBER_ID 
= 
(SELECT MEMBER_ID
FROM REST_REVIEW
GROUP BY MEMBER_ID
ORDER BY COUNT(*) DESC
LIMIT 1) 
ORDER BY REVIEW_DATE, REVIEW_TEXT